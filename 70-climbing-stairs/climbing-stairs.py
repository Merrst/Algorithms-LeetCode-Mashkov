class Solution:
    def climbStairs(self, n: int) -> int:
        # Обработка базовых случаев: для 1, 2 и 3 ступенек ответ равен n
        if n <= 3:
            return n
        
        # Используем две переменные для хранения результатов предыдущих шагов
        # first соответствует способам для (n-2), second для (n-1)
        # Для n=3: на 1-ю ступеньку 1 способ, на 2-ю — 2 способа
        prev2, prev1 = 1, 2
        
        # Итерируемся от 3 до n включительно
        for i in range(3, n + 1):
            # Количество способов для текущей ступеньки — сумма двух предыдущих
            current = prev1 + prev2
            # Обновляем значения для следующей итерации (сдвигаем окно)
            prev2 = prev1
            prev1 = current
            
        # В переменной prev1 в итоге окажется результат для ступеньки n
        return prev1
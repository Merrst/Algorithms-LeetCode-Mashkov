class Solution:
    def groupAnagrams(self, strs: list[str]) -> list[list[str]]:
        # Используем обычный словарь (хэш-таблицу)
        # Ключ: кортеж с частотой букв, Значение: список слов-анаграмм
        res = {}
        
        for s in strs:
            # Создаем список из 26 нулей для подсчета каждой буквы a-z
            count = [0] * 26 
            for c in s:
                # Вычисляем индекс буквы: 'a' -> 0, 'b' -> 1 и т.д.
                count[ord(c) - ord("a")] += 1
            
            # Преобразуем список в кортеж (tuple), так как списки нельзя 
            # использовать в качестве ключей словаря (они изменяемы)
            key = tuple(count)
            
            # Если ключа нет в словаре, создаем пустой список
            if key not in res:
                res[key] = []
            
            # Добавляем текущую строку в соответствующую группу
            res[key].append(s)
            
        # Возвращаем только накопленные группы слов
        return list(res.values())
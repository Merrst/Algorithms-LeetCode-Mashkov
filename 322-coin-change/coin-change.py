class Solution:
    def coinChange(self, coins: List[int], amount: int) -> int:
        # Инициализируем массив значениями "бесконечность" (amount + 1)
        # Индекс i в массиве dp будет означать минимальное число монет для суммы i
        dp = [amount + 1] * (amount + 1)
        
        # Базовый случай: для суммы 0 нужно 0 монет
        dp[0] = 0
        
        # Перебираем все суммы от 1 до amount
        for a in range(1, amount + 1):
            # Для каждой суммы пробуем каждую доступную монету
            for c in coins:
                # Если номинал монеты не превышает текущую сумму
                if a - c >= 0:
                    # Обновляем dp[a], выбирая минимум между текущим значением
                    # и вариантом, когда мы берем монету 'c' (1 + dp[a - c])
                    dp[a] = min(dp[a], 1 + dp[a - c])
        
        # Если значение dp[amount] осталось равным amount + 1, значит сумму собрать нельзя
        return dp[amount] if dp[amount] != (amount + 1) else -1
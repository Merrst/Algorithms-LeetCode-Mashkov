class Solution:
    def canPartition(self, nums: List[int]) -> bool:
        total_sum = sum(nums)
        
        # Если общая сумма нечетная, разделить на два равных целых подмножества невозможно
        if total_sum % 2 != 0:
            return False
            
        target = total_sum // 2
        # Множество dp хранит все возможные суммы, которые можно составить
        # Начинаем с 0, так как пустую сумму всегда можно собрать
        dp = {0}
        
        for num in nums:
            # Создаем временное множество для новых сумм на текущем шаге
            new_sums = set()
            for s in dp:
                # Если добавление текущего числа дает целевую сумму — решение найдено
                if s + num == target:
                    return True
                # Если новая сумма меньше целевой, сохраняем её для дальнейших шагов
                if s + num < target:
                    new_sums.add(s + num)
            
            # Объединяем старые суммы с новыми (аналог обновления строки в таблице DP)
            dp.update(new_sums)
            
        # Проверяем, удалось ли в итоге собрать целевую сумму
        return target in dp
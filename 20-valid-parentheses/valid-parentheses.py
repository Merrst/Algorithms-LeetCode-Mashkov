class Solution:
    def isValid(self, s: str) -> bool:
        # Стек для хранения открытых скобок
        stack = []
        # Словарь, где ключ — закрывающая скобка, а значение — открывающая
        mapping = {")": "(", "}": "{", "]": "["}

        for char in s:
            # Если видим закрывающую скобку
            if char in mapping:
                # Берем последнюю открытую из стека (или ставим заглушку '#', если стек пуст)
                top = stack.pop() if stack else '#'
                # Если они не подходят друг другу — это ошибка
                if mapping[char] != top:
                    return False
            else:
                # Если это открывающая скобка — кладем ее в "стопку" (стек)
                stack.append(char)

        # В конце проверяем: если стек пустой, значит все пары совпали
        return not stack